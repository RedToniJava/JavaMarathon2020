package day18;

public class Node {
    private int leftSon;
    private int rightSon;
    private  int node;
    public Node(int node){
        this.node=node;
            }

    public int getNode() {
        return node;
    }

    public int getLeftSon() {
        return leftSon;
    }

    public int getRightSon() {
        return rightSon;
    }
}
